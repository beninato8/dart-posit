    }

    void checkPosit(PositTestItem item, int nbits, int es) {
      final PositTestItem(:bits, :sign, :k, :regime, :exponent, :fraction, :value) = item;
      final posit = Posit.fromBits(bits, nbits: nbits, es: es);
      expect(posit.bits, equals(bits), reason: 'bits: ${posit.bits.toRadixString(2).padLeft(nbits, '0')}');
      expect(posit.signBit, equals(sign), reason: 'sign: ${posit.signBit.toRadixString(2)}');
      expect(posit.k, equals(k), reason: 'k: ${posit.k}');
      expect(posit.regime, equals(regime), reason: 'regime: ${posit.regime.toRadixString(2)}');
      expect(posit.exponent, equals(exponent), reason: 'exponent: ${posit.exponent.toRadixString(2)}');
      expect(posit.fractionValue, equals(fraction), reason: 'fraction: ${posit.fractionValue}');
      checkPositValue(posit, value);
    }

